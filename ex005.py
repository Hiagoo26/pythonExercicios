#Antecessor e Sucessor
n = int(input('Insira um numero: '))
ant = n - 1
suc = n + 1
print('O Antecessor do numero {} é {}, e o sucessor é {}' .format(n, ant, suc))

#OU
n2 = int(input('Insira um numero: '))
print('O numero {} tem como antecessor o {} e como sucessor o {}' .format(n2, (n2-1), (n2+1)))